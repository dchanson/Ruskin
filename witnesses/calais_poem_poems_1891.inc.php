   <div class="title" style="text-align:center"><span class="caps">CALAIS</span>.
   </div>
   <span class="lg-stanza"> 
      <span class="l">T<span class="smallcaps">HE</span> sands are in the sunlight sleeping,</span><br/> 
      <span class="l">&#x005B;The tide upon the bar is leaping;&#x005D;<span class="glosses"><a href="../glosses/calais_glosses.html#CALAISPOEMLINE2" target="_blank"" class="inactive">1</a></span></span><br/> 
      <span class="l">Again, again for evermore</span><br/> 
      <span class="l">Haste the light curlings to the shore,</span><br/>
      <div id="" class="pb"></div><span class="l">And yet advance and yet retreat</span><br/>
      <span class="l">On playful childhood&#x02BC;s daring feet,</span><br/> 
      <span class="l">That seeks within its sandy cell</span><br/>
      <span class="l">The pebble bright, or purple shell.<span class="glosses"><a href="../glosses/calais_glosses.html#CALAISPOEMSHELL" target="_blank"" class="inactive">2</a></span></span><br/> 
      <span class="l">Far in its clear expanse, lay wide</span><br/> 
      <span class="l">Unruffledly that ocean tide,</span><br/> 
      <span class="l">Stretching away where paler grew</span><br/> 
      <span class="l">The heaven&#x02BC;s bright unclouded blue.</span><br/>
      <span class="l">And, far away in distance dying,</span><br/>
      <span class="l">Old <span class="placeName-ENGLAND">England</span>&#x02BC;s cliffy coast was lying;</span><br/>
      <span class="l">And beautiful as summer cloud</span><br/>
      <span class="l">By the low sun empurpled proud.</span><br/>
      </span>
   <span class="lg-stanza">    
      <span class="l">Strange, that a space from shore to shore</span><br/>
      <span class="l">So soon, so easily passed o&#x02BC;er,</span><br/> 
      <span class="l">Should yet a wide distinction place</span><br/>
      <span class="l">&#x02BC;Twixt man and man, &#x02BC;twixt race and race!</span><br/>
      <span class="l">Sudden and marked the change you find,&#x2014;</span><br/>
      <span class="l">Religion, language, even mind;</span><br/>
      <span class="l">That you might think that narrow span</span><br/>
      <span class="l">Marked the varieties of man.</span><br/>
      </span>
   
</div>


