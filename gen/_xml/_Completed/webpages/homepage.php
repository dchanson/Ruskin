               <h2 class="line-beginning">The Early Ruskin Manuscripts, 1826&#x2013;1842</h2> is an archive of the youthful writing of John Ruskin (1819&#x2013;1900).
                    When complete, <i>ERM</i> will present a comprehensive diplomatic edition of Ruskin&#x02BC;s early
                    writing paired with facsimiles of the manuscripts. The documentary record is distinguished by
                    its extraordinary level of achievement and its broad range of genres and disciplines, 
                    including poetry, travel writing, religion, earth sciences, and mathematics. Thus, 
                    the archive provides a resource for the study of Ruskin&#x02BC;s education and development as a writer in particular, 
                    and for the study of Victorian juvenilia, education, and culture in general.
                </div>
            
