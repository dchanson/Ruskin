                  <div id="DISCUSSION" class="section">
                     <span class="head-section">Discussion</span>
                     
                     <div class="p">In the travel narrative in <a href="/apparatuses/harry_and_lucy_vol2_apparatus" target="_self"><span class="title-lesson-HARRYANDLUCYVOL2">&#x201C;Harry and Lucy&#x201D;, Vol. 2</span></a>, 
                        the narrator mentions crossing the sea to get from <span class="placeName-WALES">Wales</span> to <span class="placeName-SCOTLAND">Scotland</span>, 
                        although it is not clear where this voyage occurred in the Ruskins' actual tour. See <a href="/notes/tours_of_1826-27_note#WALESANDSCOTLAND" target="_self">Tours of 1826&#x2013;27: Wales and Scotland, 1827</a>.
                    
                     </div>
                     
                     <div class="p">See also <a href="/notes/poetry_discriptive_apparatus" target="_self">Poetry Descriptive</a> for how 
                        <span class="title-poem-THESEA">&#x201C;The Sea&#x201D;</span> and <span class="title-poem-THESTORM">&#x201C;The Storm&#x201D;</span> together 
                        form a conclusion to the anthology.
