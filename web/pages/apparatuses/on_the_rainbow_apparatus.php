                  <div id="TITLE" class="section">
                     <span class="head-section">Title</span>
                     
                     <div class="p"><span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span></div>
                     
                     <div class="p"><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-ONTHERAINBOW">&#x201C;on the rainbow / in blank verse&#x201D;</span>. 
                        He also designated the poem as &#x201C;poem VI&#x201D; in
                        <a href="/apparatuses/msi_poetry_anthology_apparatus" target="_self"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>.
                     </div>
                     
                     <div class="p">See also <a href="/notes/title_citation_note" target="_self">System of Title Citation for Works</a>.</div>
                     
                  </div>
                  
                  <div id="GENRE" class="section">
                     <span class="head-section">Genre</span>
                     
                     <div class="p">Poem. <span class="persName-JR">Ruskin</span> applied the term <i>blank verse</i> also to 
                        <a href="/apparatuses/time_blank_verse_apparatus" target="_self"><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span></a>
                        and to <a href="/apparatuses/spring_blank_verse_apparatus" target="_self"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>.
                     </div>
                     
                  </div>
                  
                  
                  <div id="MAJORMS" class="section">
                     <span class="head-section">Manuscripts</span>
                     
                     <div class="p"><a href="/apparatuses/msi_apparatus" target="_self"><span class="title-manuscript-MSI">MS I</span></a> (pp. 104&#x2013;6), 
                        a <a href="/notes/red_book_note" target="_self">Red Book</a>
                        devoted primarily devoted to <a href="/apparatuses/harry_and_lucy_vol1_apparatus" target="_self"><span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy&#x201D; Vol. 1</span></a>. 
                        <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span> is the sixth poem in 
                        <a href="/apparatuses/msi_poetry_anthology_apparatus" target="_self"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>.
                     </div>
                     
                     <div class="p">Facsimiles by permission of <span class="title-archive-BEINECKERUSKINCOLLECTION">John Ruskin Collection</span>, General Collection, Beinecke Rare Book and Manuscript Library, Yale University. Transcriptions of texts and commentary &#x00A9; <span class="persName-DCH">David C. Hanson</span>.
                     </div>
                     
                  </div>
                  
                  <div id="DATE" class="section">
                     <span class="head-section">Date</span>
                     
                     <div class="p"><span class="date-1827-01-1827-03">January&#x2013;March</span>, perhaps specifically <span class="date-1827-03-21">March 21</span>. 
                        There is a possibility that the poem originated in <span class="date-1826-01-1826-04">early 1826</span>.
                     </div>
                     
                     <div class="p">See <a href="/apparatuses/on_papas_leaving_home_apparatus#DATE" target="_self">&#x201C;On Papa&#x02BC;s Leaving Home&#x201D;: Date</a>. If that poem can be
                        attributed to <span class="date-1827-01-1827-02">January&#x2013;February 1827</span>, but with an outside possibility of its belonging (like potentially all the poems in 
                        <a href="/apparatuses/msi_poetry_anthology_apparatus" target="_self"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>) 
                        to sometime in <span class="date-1826">1826</span>, <span class="date-1827-01-1827-03">January&#x2013;March 1827</span> range bears an even stronger likelihood for 
                        <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span>, since this poem shares the subtitle &#x201C;Blank Verse&#x201D; with
                        both <a href="/apparatuses/time_blank_verse_apparatus" target="_self"><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span></a>, 
                        which <span class="persName-JR">Ruskin</span> prepared as a 
                        <a href="/notes/new_years_poems" target="_self">New Year&#x02BC;s poem</a> for <span class="date-1827-01-01">1 January 1827</span>, 
                        and <a href="/apparatuses/spring_blank_verse_apparatus" target="_self"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>, 
                        which <span class="persName-JR">Ruskin</span> composed in <span class="date-1827-05">May 1827</span>.
                     </div>
                     
                     <div class="p">Moreover, if <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span> complements the drawing on the back endboard of 
                        <a href="/apparatuses/msi_apparatus" target="_self"><span class="title-manuscript-MSI">MS I</span></a>, 
                        <a href="/figures/heights_of_wisdom_drawing_msi" target="_self">&#x201C;Heights of Wisdom, Depth of Fools&#x201D;</a>
                        (see <a href="#DISCUSSION">Discussion</a>), then the poem&#x02BC;s date can be pinpointed at <span class="date-1827-03-21">21 March 1827</span>.
                     </div>
                     
                     <div class="p">See <a href="/notes/date_citation_note" target="_self">System of Date Citation</a>.</div>
                     
