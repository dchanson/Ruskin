               <div id="COMPPUB" class="section">
                  <span class="head-section">Composition and Publication</span>
                  
                  <div class="p">First published with an omission and editor&#x02BC;s punctuation in 
                        <a href="/notes/bibliography#WGCLIFEOFJR1900" target="_self"><span class="persName-WGC">Collingwood</span>, <span class="title-book-WGCLIFEOFJR1900"><i>The Life of John Ruskin</i></span> (rev. 1900)</a>, 18&#x2013;19; 
                        and reprinted nearly identically (with a punctuation change) in 
                        <a href="/notes/bibliography#JRWORKSLE" target="_self"><span class="persName-JR">Ruskin</span>, <span class="title-book-JRWORKSLE"><i>Works</i></span></a>, 1:xxvi n. ?.
                        Edited without omission and editorial punctuation, and for the first time along with <span class="persName-MR">Margaret Ruskin</span>&#x02BC;s extension 
                        consisting of her own letter to <span class="persName-JJR">John James</span>, in 
                        <a href="/notes/bibliography#RFL" target="_self"><span class="persName-VAB">Burd</span>, ed., <span class="title-book-RFL"><i>Ruskin Family Letters</i></span></a>, 127&#x2013;28. 
                        Partially reproduced in <a href="/notes/bibliography#DEARDENRUSKINGALLERIES" target="_self"><span class="persName-JSD">James S. Dearden</span>, <span class="title-essay-DEARDENRUSKINGALLERIES">&#x201C;The <span class="persName-JR">Ruskin</span> Galleries at Bembridge School, Isle of Wight&#x201D;</span></a>, pl. III.
                  </div>
                  
                  
                  <div class="p">Except for the signature, which <span class="persName-JR">Ruskin</span> wrote in pencil and in large capitals, his letter is in <span class="persName-MR">Margaret</span>&#x02BC;s hand. 
                        His salutation, letter, and signature are placed first on the sheet, followed by <span class="persName-MR">Margaret</span>&#x02BC;s, in which she explains 
                        the circumstances of composition:
                  </div>
                  
                  
                  <div class="floating-text">
                     
                     <div class="letter" id="">
                        
                        <div class="p">Your boy has been very busy scrawling with a pencil on a piece of paper 
                                    which he said was a letter to send to you  I told him I was afraid you would not be able to make it out 
                                    and he said he would read it to me if I would write it to you  the above is exactly word for word 
                                    what he prentended to read from his paper  the signature you will see is his own  sometimes he makes the letters 
                                    much better  he is beginning to copy from his books and will soon learn himself to write I think&#x2014;
                                </div>
                        
                        <div class="p">(<a href="/notes/bibliography#RFL" target="_self"><span class="persName-VAB">Burd</span>, ed., <span class="title-book-RFL"><i>Ruskin Family Letters</i></span></a>, 128)
                        </div>
                        
                     </div>
                     
                  </div>
                  
                  
                  
               </div>
               
