                  <div id="TITLE" class="section">
                     <span class="head-section">Title</span>
                     
                     <div class="p"><span class="title-poem-ONSCOTLAND">&#x201C;On Scotland&#x201D; &#x005B;&#x201C;Farewell to Scotland&#x201D;&#x005D;</span></div>
                     
                     <div class="p"><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-ONSCOTLAND">&#x201C;on scotland&#x201D;</span>. He also
                        designated the poem as &#x201C;poem II&#x201D; in <a href="/apparatuses/msi_poetry_anthology_apparatus" target="_self"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>. 
                        The title &#x201C;Farewell to Scotland&#x201D; was invented by <a href="/notes/collingwood_w_g_note" target="_self"><span class="persName-WGC">W. G. Collingwood</span></a>
                        for <a href="/notes/poems_1891_note" target="_self"><i>Poems</i> (1891)</a>.
                     </div>
                     
                     <div class="p">See also <a href="/notes/title_citation_note" target="_self">System of Title Citation for Works</a>.</div>
                     
                  </div>
                  
                  <div id="GENRE" class="section">
                     <span class="head-section">Genre</span>
                     
                     <div class="p">Poem; <a href="/notes/topographical_poetry_note" target="_self">topographical poem</a>.</div>
                     
                     <div class="p">Rhyming couplets; iambic pentameter. See <a href="#DISCUSSION">Discussion</a> for possible connection with travel.
                     </div>
                     
                  </div>
                  
