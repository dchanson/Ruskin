               <div id="DATE" class="section">
                  <span class="head-section">Date</span>
                  
                  <div class="p"><span class="date-1832-07-1833-05-10">Sometime between July 1832 and April or early May 1833</span>, and within that span perhaps closer to <span class="date-1833-04">April 1833</span>. 
                        In <a href="/notes/poems_1850_note" target="_self"><span class="title-book-JRPOEMS1850"><i>Poems</i></span> by J.R. (1850)</a>, 
                        the <span class="title-poem-JRSONGIWEARY">&#x201C;Song&#x201D;</span> is ascribed to &#x201C;aetat. fourteen&#x201D;&#x2014;that is, 
                        <span class="date-1833-02-19-1833-02-19">between 19 February 1833 and February 1834</span> 
                        (<a href="/notes/bibliography#POEMS1850" target="_self"><span class="title-book-JRPOEMS1850"><i>Poems</i> by J.R. &#x005B;1850&#x005D;</span></a>, 3).
                  </div>
                  
                  
                  <div class="p"><a href="/notes/collingwood_w_g_note" target="_self"><span class="persName-WGC">W. G. Collingwood</span></a> 
                        dated the poem earlier than the date ascribed in <span class="title-book-JRPOEMS1850"><i>Poems</i> by J.R.</span>, assigning the poem to &#x201C;some time before the author actually reached the age of fourteen&#x201D;, suggesting that <span class="persName-JR">Ruskin</span> wrote it in <span class="date-1832-12-1833-01">&#x201C;winter 1832&#x2013;33&#x201D;</span>. 
                        <span class="persName-WGC">Collingwood</span> based his rationale on &#x201C;the position of the rough draft&#x201D; in <span class="title-manuscript-MSVIII">MS VIII</span>, 
                        and on the supposed &#x201C;fact that the author had not been among mountains that year, but only to <span class="placeName-DOVER">Dover</span> and <span class="placeName-HASTINGS">Hastings</span>&#x201D;, 
                        thus accounting for the poem&#x02BC;s nostalgia (<a href="/notes/bibliography#POEMS4D1891" target="_self"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1:115, 280; 
                        <a href="/notes/bibliography#POEMS8D1891" target="_self"><i>Poems</i> &#x005B;8<sup>o</sup>, 1891&#x005D;</a>, 1:116, 282; 
                        and see <a href="/notes/seaside_resorts_note" target="_self">The Ruskins&#x02BC; Visits to Seaside Resorts and Spas</a>).
                  </div>
                  
                  
                  <div class="p">The poem&#x02BC;s position in <span class="title-manuscript-MSVIII">MS VIII</span> allows for a broad span of possible times of composition, <span class="date-1832-07-1833-05-10">from July 1832 to April or early May 1833</span> 
                        (see <a href="#COMPPUB">Composition and Publication</a>).
                  </div>
                  
                  
               </div>
               
               <div id="COMPPUB" class="section">
                  <span class="head-section">Composition and Publication</span>
                  
                  <div class="p">The poem was first published in <a href="/notes/bibliography#POEMS1850" target="_self"><span class="title-book-JRPOEMS1850"><i>Poems</i> by J.R. (1850)</span></a>, 
                        having been selected by the editors as the opening poem of that collection (pp. 3&#x2013;4). It was reprinted in 
                        <a href="/notes/bibliography#POEMS4D1891" target="_self"><span class="persName-WGC">W. G. Collingwood</span>, ed., <i>Poems</i> (4<sup>o</sup>, 1891)</a>, 1:115&#x2013;16; 
                        <a href="/notes/bibliography#POEMS8D1891" target="_self"><i>Poems</i> (8<sup>o</sup>, 1891)</a>, 1:115&#x2013;16; and 
                        <a href="/notes/bibliography#JRWORKSLE" target="_self">Ruskin, <i>Works</i></a>, 2:3&#x2013;4. 
                        In <span class="date-1879">1879</span>, the scholar and book collector <a href="/notes/axon_w_e_a_note" target="_self">W. E. A. Axon (<span class="date-1846-1913">1846&#x2013;1913</span>)</a> 
                        featured the poem in <a href="/notes/bibliography#AXONJRBIBLIOGRAPHICALBIO" target="_self"><span class="title-essay-AXONJRBIBLIOGRAPHICALBIO">&#x201C;John Ruskin: A Bibliographical Biography&#x201D;</span></a>, 
                        taking the text from <span class="title-book-JRPOEMS1850"><i>Poems</i> by J.R.</span></div>
                  
                  
                  <div class="p">In <span class="title-manuscript-MSVIII">MS VIII</span>, <span class="title-poem-JRSONGIWEARY">&#x201C;I weary for the torrent leaping&#x201D;</span> along with the poem 
                        <a href="/apparatuses/have_ye_never_heard_of_the_brownie_wight_apparatus" target="_self"><span class="title-poem-JRBROWNIEWIGHT">&#x201C;Have ye never heard of the Brownie wight&#x201D;</span></a> 
                        is situated between the poems <a href="/apparatuses/through_the_stone_and_ancient_gate_apparatus" target="_self"><span class="title-poem-JRSTONEANDANCIENTGATE">&#x201C;Through the stone and ancient gate&#x201D;</span></a> 
                        and <a href="/apparatuses/my_fathers_birthday_the_month_of_may_apparatus" target="_self"><span class="title-poem-JRMONTHOFMAY">&#x201C;My Father&#x02BC;s Birthday: The Month of May&#x201D;</span></a>, the latter a birthday ode for <span class="date-1833-05">May 1833</span>. 
                        This section of the manuscript (<a href="/apparatuses/msviii_apparatus#CONTENTS" target="_self">List <i>a</i>.2</a>), as compared to a preceding section 
                        (<a href="/apparatuses/msviii_apparatus#CONTENTS" target="_self">List <i>a</i>.1</a>) that was composed during <span class="date-1831-1832">1831&#x2013;32</span>, represents a broad span of time, 
                        possibly stretching from the family&#x02BC;s return from the <a href="/notes/tour_of_1832_note" target="_self">Tour of 1832</a> at the end of <span class="date-1832-06-30">June 1832</span> 
                        to <span class="persName-JJR">John James</span>&#x02BC;s birthday in <span class="date-1833-05-10">May 1833</span>. In this space of possibly 
                        ten or eleven months, fewer than ten poems occupy this section of the notebook, all of them brief and some fragmentary (see <a href="/apparatuses/msviii_apparatus#COLLATIONA" target="_self">MS VIII: Section <i>a</i></a>).
                  </div>
                  
                  
                  <div class="p">Within that sequence, the position of <span class="title-poem-JRSONGIWEARY">&#x201C;I weary for the torrent leaping&#x201D;</span> suggests 
                        that <span class="persName-JR">Ruskin</span> composed it closer to <span class="date-1833-04">April 1833</span>, when he would have drafted <span class="title-poem-JRMONTHOFMAY">&#x201C;My Father&#x02BC;s Birthday: The Month of May&#x201D;</span>. 
                        This inference confirms the poem&#x02BC;s association with age fourteen according to <span class="title-book-JRPOEMS1850"><i>Poems</i> by J.R.</span> 
                        At this time, <span class="persName-JR">Ruskin</span> would have been eagerly anticipating the family&#x02BC;s departure for the <a href="/notes/tour_of_1833_note" target="_self">Tour of 1833</a>, 
                        their first major tour of the Continent.
                  </div>
                  
                  
                  <div class="p">The <span class="title-manuscript-MSVIII">MS VIII</span> draft is annotated in the hand of <span class="persName-JJR">John James Ruskin</span>, 
                        who penciled numbers next to stanzas, indicating his rearrangement of the poem for <a href="/notes/poems_1850_note" target="_self"><span class="title-book-JRPOEMS1850"><i>Poems</i></span> by J.R. (1850)</a> (see textual glosses). 
                        
                     
                  </div>
                  
               </div>
               
               <div id="DISCUSSION" class="section">
                  <span class="head-section">Discussion</span>
                  
                  <div class="p">The poem&#x02BC;s topic may have to do less with yearning for mountain scenery than with desire to compose verse about scenery of any kind. 
                        <span class="persName-WGC">Collingwood</span> thought it &#x201C;curious that &#x005B;<span class="persName-JR">Ruskin</span>&#x02BC;s&#x005D; mountain&#x2010;yearning 
                        does not carry him back to <span class="geogName-SNOWDON">Snowdon</span>&#x201D;, a highlight of the 
                        <a href="/" target="_self">Tour of 1831</a>, which included <span class="placeName-WALES">Wales</span>, 
                        &#x201C;but to earlier visions of the <span class="geogName-LAKEDISTRICT">Lake Country</span>, his first and last mountain&#x2010;love&#x201D; 
                        (<a href="/notes/bibliography#POEMS4D1891" target="_self"><i>Poems</i> &#x005B;4<sup>o</sup>, 1891&#x005D;</a>, 1:280; 
                        <a href="/notes/bibliography#POEMS8D1891" target="_self"><i>Poems</i> (8<sup>o</sup>, 1891)</a>, 1:282). 
                        The yearning is not just for place, however, but also for song: &#x201C;My harp is sunk to rest&#x201D; and &#x201C;My lyres voice is still&#x201D;.
                  </div>
                  
                  
                  <div class="p">In <span class="date-1832">1832</span>, <span class="persName-JR">Ruskin</span>&#x02BC;s parents decided to check the amount of time and energy 
                        <span class="persName-JR">Ruskin</span> could devote to poetry composition. Concern had arisen, particularly on <span class="persName-JJR">Margaret Ruskin</span>&#x02BC;s part, 
                        over <span class="persName-JR">John</span>&#x02BC;s voluminous versifying during <span class="date-1831-1832">1831&#x2013;32</span>, 
                        in which he was encouraged by his <span class="persName-JJR">father</span>&#x02BC;s rewards of a farthing or halfpenny per line of poetry. 
                        Now, in <span class="date-1833-01">January 1833</span>, <span class="persName-JJR">John James</span> repentantly wrote to a friend, &#x201C;We restrain his poetic Efforts&#x201D;, 
                        even as the proud father enclosed a sample of <span class="persName-JR">John</span>&#x02BC;s writing (a letter, not a poem), and boasted how his 
                        &#x201C;Talents&#x201D; were &#x201C;great for his age&#x201D; (letter to <span class="persName-RG">Richard Gray</span>, <span class="date-1833-01-17">17 January 1833</span>, 
                        in <a href="/notes/bibliography#RFL" target="_self"><span class="persName-VAB">Burd</span>, ed., <span class="title-book-RFL"><i>Ruskin Family Letters</i></span></a>, 276; and see 
                        <a href="/notes/bibliography#HANSONPSYCHOLOGY" target="_self"><span class="persName-DCH">Hanson</span>, <span class="title-essay-HANSONPSYCHOLOGY">&#x201C;Psychology of Fragmentation&#x201D;</span></a>).
                  </div>
                  
                  
                  <div class="p">In <span class="date-1849">1849</span>, when preparing the <a href="/notes/poems_1850_note" target="_self"><span class="title-book-JRPOEMS1850"><i>Poems</i></span> by J.R. (1850)</a>, 
                        <span class="persName-JJR">John James</span> may have remembered the circumstances of this poem&#x02BC;s composition. If so, he was playing a complicated family joke by placing the <span class="title-poem-JRSONGIWEARY">&#x201C;Song&#x201D;</span> at the head of the collection. 
                        <span class="persName-JR">Ruskin</span> had now silenced himself as a poet, abandoning his &#x201C;lyre&#x02BC;s voice&#x201D; for prose composition.
                  </div>
                  
