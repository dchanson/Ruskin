               <div id="TITLE" class="section">
                  <span class="head-section">Title</span>
                  
                  <div class="p">The poem is untitled in the <span class="title-manuscript-MSVIII">MS VIII</span> draft.
                  </div>
                  
                  <div class="p">The title <span class="title-poem-JRSONGIWEARY">&#x201C;Song&#x201D;</span> was first applied to the poem in 
                        <a href="/notes/poems_1850_note" target="_self"><span class="title-book-JRPOEMS1850"><i>Poems</i></span> by J.R. (1850)</a>&#x2014;at whose suggestion is unknown. 
                        This title was retained in all subsequent reprintings.
                  </div>
                  
               </div>
               
               <div id="GENRE" class="section">
                  <span class="head-section">Genre</span>
                  
                  <div class="p">Poem; <i>abab</i>, alternating tetrameter and trimeter.
                  </div>
                  
               </div>
               
