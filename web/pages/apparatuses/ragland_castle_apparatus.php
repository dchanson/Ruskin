                  <div id="TITLE" class="section">
                     <span class="head-section">Title</span>
                     
                     <div class="p">&#x201C;Ragland Castle&#x201D;</div>
                     
                     <div class="p"><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-">&#x201C;ragland castle&#x201D;</span>.
                     </div>
                     
                     <div class="p">See <a href="/notes/title_citation_note" target="_self">System of Title Citation for Works</a>.</div>
                     
                     <div class="p">While today the preferred spelling of the name of this castle and village in <span class="placeName-WALES">Wales</span> is 
                        <i><span class="placeName-RAGLAN">Raglan</span></i> or the Welsh <i><span class="placeName-RAGLAN">Rhaglan</span></i>, 
                        <span class="persName-JR">Ruskin</span>&#x02BC;s spelling, <i><span class="placeName-RAGLAN">Ragland</span></i>, 
                        would have seemed correct to nineteenth&#x2010;century English tourists. 
                        For example, the spelling <i><span class="placeName-RAGLAN">Ragland</span></i> appears in two important maps of the period: 
                        the <span class="title-map-GREENWOODMAPMONMOUTH-italic">Map of the County of Monmouth, from an Actual Survey Made in the Years 1829 &#x0026; 1830</span> 
                        (<span class="placeName-LONDON">London</span>, <span class="date-1831">1831</span>) by the Greenwood brothers, 
                        <span class="persName-CGCARTOGRAPHER">Christopher</span> (<span class="date-1786-1855">1786&#x2013;1855</span>) and <span class="persName-JGCARTOGRAPHER">John</span> (<span class="date-1791-1867">1791&#x2013;1867</span>); 
                        and the <span class="title-map-ORDINANCESURVEYMAPS-italic">Ordinance Survey &#x201C;Old Series&#x201D;</span> maps (sheet 43 &#x005B;<span class="placeName-LONDON">London</span>, <span class="date-1831">1831</span>&#x005D;). 
                        In tourism literature, the spelling was used by the <span class="placeName-MONMOUTH">Monmouth</span> printer, <span class="persName-CHMONMOUTH">Charles Heath</span> (<span class="date-1761-1830">1761&#x2013;1830</span>), 
                        in his <i><span class="title-guidebook-HEATHRAGLANDCASTLE">Historical and Descriptive Accounts of the Ancient and Present State of Ragland Castle</span></i> (<span class="date-1792">1792</span>), 
                        a publication that preceded <span class="persName-CHMONMOUTH">Heath</span>&#x02BC;s popular guidebook to <span class="placeName-TINTERN">Tintern Abbey</span>, 
                        <i><span class="title-guidebook-HEATHTINTERN">Descriptive Account of Tintern Abbey</span></i> (<span class="date-1793">1793</span>). 
                        <span class="persName-CHMONMOUTH">Heath</span>&#x02BC;s guidebooks to these monuments lasted through many editions <span class="date-1790-1829">from the 1790s through the 1820s</span> 
                        (see &#x201C;&#x2018;The Picture of the Mind&#x2019;: Tintern and Vicinity through Images&#x201D; and 
                        &#x201C;&#x2018;Gleams of Past Existence&#x2019;: Charles Heath&#x02BC;s Guide to Tintern Abbey&#x201D; in 
                        <a href="/notes/bibliography#MATHESONENCHANTINGRUIN" target="_self">Matheson, <i>Enchanting Ruin</i></a>; see also 
                        <a href="/notes/bibliography#HEBRONROMANTICSANDBRITISHLANDSCAPE" target="_self">Hebron, <i>Romantics and the British Landscape</i>, 10&#x2013;11</a>). 
                        In his guidebook to the castle, <span class="persName-CHMONMOUTH">Heath</span> traced the Welsh etymology of the name and remarked that, in the process of &#x201C;Englishiz&#x005B;ing&#x005D;&#x201D; the name, 
                        &#x201C;within these very few years . . . the letter D has been added to it&#x201D; 
                        (&#x201C;Etymology&#x201D; in <a href="/notes/bibliography#HEATHRAGLANDCASTLE" target="_self">Heath, <i>Historical and Descriptive Accounts of the Ancient and Present State of Ragland Castle</i> &#x005B;11th ed., <span class="date-1829">1829</span>&#x005D;</a>, n.p.).
                    
                     </div>
                     
                  </div>
                  
