                  <div id="DATE" class="section">
                     <span class="head-section">Date</span>
                     
                     <div class="p"><span class="date-1827-07-01-1827-12-31">1827, after June</span>.
                     </div>
                     
                     <div class="p">The editors of the <a href="/notes/library_edition_note" target="_self">Library Edition</a> were mistaken
                        in calling this &#x201C;possibly the <span class="persName-JR">author</span>&#x02BC;s earliest poem&#x201D; 
                        (<a href="/notes/bibliography#JRWORKSLE" target="_self">Ruskin, <i>Works</i></a>, 2:255&#x2013;56 n. 1; and see 
                        <a href="/apparatuses/the_needless_alarm_apparatus#DATE" target="_self"><span class="title-poem-THENEEDLESSALARM">&#x201C;The Needless Alarm&#x201D;</span>: Date</a> for the case for
                        <span class="persName-JR">Ruskin</span>&#x02BC;s earliest extant poem). There is no reason to believe that
                        <span class="title-poem-">&#x201C;Ragland Castle&#x201D;</span> is earlier than the anthology containing it, 
                        <a href="/apparatuses/poetry_discriptive_apparatus" target="_self"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>.
                     </div>
                     
                     <div class="p"><a href="/notes/viljoen_h_g_note" target="_self"><span class="persName-HGV">Helen Gill Viljoen</span></a>
                        believed that, because the hand for this poem is smaller than for that characterizing other poems in 
                        <a href="/apparatuses/poetry_discriptive_apparatus" target="_self"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>, 
                        <span class="persName-JR">Ruskin</span> may have added it later to the front of the anthology 
                        (<a href="/notes/bibliography#VILJOENPAPERS" target="_self">Viljoen Papers</a>).
                        The argument is not convincing, since no crowding of lines suggests a later insertion, and the hypothesis creates a new problem of explaining why the following poem, 
                        <a href="/apparatuses/lochleven_apparatus" target="_self"><span class="title-poem-LOCHLEVENPOEM">&#x201C;Lochleven&#x201D;</span></a> would have begun so far down on the page. 
                        In fact, the size of the lettering varies even within this fair copy of <span class="title-poem-">&#x201C;Ragland Castle&#x201D;</span>, 
                        and its smallest printing is no smaller than for some other portions of <a href="/apparatuses/poetry_discriptive_apparatus" target="_self"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>.
                     </div>
                     
                     <div class="p">See <a href="/notes/date_citation_note" target="_self">System of Date Citation</a>.</div>
                     
