               <div id="DATE" class="section">
                  <span class="head-section">Date</span>
                  
                  <div class="p">A draft appears as the first item in the handmade booklet, <span class="title-manuscript-MSII">MS II</span>, preceding draft of 
                        <a href="/apparatuses/harry_and_lucy_vol3_apparatus" target="_self"><span class="title-lesson-JRHARRYANDLUCYVOL3">&#x201C;Harry and Lucy, Vol. 3&#x201D;</span></a>, 
                        which <span class="persName-JR">Ruskin</span> must have been written toward the end of <span class="date-1828">1828</span>. Substitutions in this draft are accounted for in a fair copy in <span class="title-manuscript-MSIII">MS III</span>, 
                        so the latter witness definitely dates from a later time, 
                        likely from around <span class="date-1828-03-09">9 March 1829</span>, since it is included in a group of poems given that date in manuscript 
                        (see <a href="/" target="_self"><span class="title-poem-JROFROCKSFIRST">&#x201C;Of rocks first and of caverns now I sing&#x201D;</span></a>).
