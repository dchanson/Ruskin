               <div id="DISCUSSION" class="section">
                  <span class="head-section">Discussion</span>
                  
                  <div class="p">See the discussion of an earlier poem, <a href="/apparatuses/look_at_that_ship_apparatus#DISCUSSION" target="_self"><span class="title-poem-JRTHESHIP1827">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at That Ship&#x201D; &#x005B;<span class="date-1827">1827</span>&#x005D;</span></a>.
                        The earlier poem shares a similar, though not identical, set of first six lines with the present poem; thereafter, however, the new poem swerves in a quite different direction, 
                        so much so that the new poem cannot be considered a version of the earlier one.
                  </div>
                  
                  <div class="p"><span class="persName-JR">Ruskin</span> was inspired to this revision by a sight encountered during the 
                        <a href="/notes/tour_of_1828_note" target="_self">Tour of <span class="date-1828">1828</span></a>. As he in the 
                        <a href="/apparatuses/travel_itinerary_and_tour_notes_1828_apparatus" target="_self"><span class="title-manual_guide-JRTRAVELITINERARY1828">Travel Itinerary and Tour Notes &#x005B;<span class="date-1828">1828</span>&#x005D;</span></a>, 
                        the family, when crossing from <span class="placeName-PORTSMOUTH">Portsmouth</span> to the <span class="placeName-ISLEOFWIGHT">Isle of Wight</span>, 
                        could see the &#x201C;place where <span class="name--ROYALGEORGE">royal george</span> sunk,&#x201D; a catastrophe incorporated into the revised poem. 
                        The site of this famous disaster, which occurred in <span class="date-1782">1782</span>, is pointed out in travel guides published throughout the nineteenth century, 
                        long after the ship&#x02BC;s wreckage presented a visible reminder of the catastrophe. See the glosses to the poem for details.
                  </div>
                  
                  <div class="p">At line 21 of the <span class="title-manuscript-MSIII">MS III</span> version of <span class="title-poem-JRTHESHIP182829">&#x201C;The Ship&#x201D; &#x005B;<span class="date-1828-1829">1828&#x2010;29</span>&#x005D;</span>, 
                        a line that starts a new page (p. 40) in that manuscript, <span class="persName-JR">Ruskin</span> abruptly reduces the size of his lettering and squeezes the lines closer together, 
                        possibly suggesting that he was running out of room for this fair copy. Nothing in the <span class="title-manuscript-MSII">MS II</span> draft indicates that <span class="persName-JR">Ruskin</span> suddenly decided to lengthen the poem. 
                        In <span class="title-manuscript-MSIII">MS III</span>, perhaps he had already begun to fair&#x2010;copy  
                        <a href="/apparatuses/sonnet_to_the_sun_apparatus" target="_self"><span class="title-poem-SONNETTOTHESUN">&#x201C;Sonnet to the Sun&#x201D;</span></a>, which follows 
                        <span class="title-poem-JRTHESHIP182829">&#x201C;The Ship&#x201D; &#x005B;<span class="date-1828-1829">1828&#x2010;29</span>&#x005D;</span> 
                        in the anthology, but discovered that he had left insufficient space.
                  </div>
                  
