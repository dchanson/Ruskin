                  <div id="TITLE" class="section">
                     <span class="head-section">Title</span>
                     
                     <div class="p">Untitled in draft. When a revised version was published in the literary annual, 
                        <a href="/notes/friendships_offering_note" target="_self"><i>Friendship&#x02BC;s Offering</i></a> 
                        (see <a href="COMPPUB">Composition and Publication</a>), 
                        the poem first exhibited its title, &#x201C;Saltzburg&#x201D;; however, this title must have been determined by that of the steel engraving, 
                        which the poem was commissioned to accompany.
                     </div>
                     
                  </div>
                  
