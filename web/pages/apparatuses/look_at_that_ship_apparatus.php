                  <div id="DISCUSSION" class="section">
                     <span class="head-section">Discussion</span>
                     
                     <div class="p">While the <a href="/apparatuses/msxi_apparatus" target="_self"><span class="title-manuscript-MSXI">MS XI</span></a> witness apparently served as a presentation copy, 
                    no inscription indicates the occasion. A clue is provided by the docketing, probably by <span class="persName-MR">Margaret Ruskin</span>, dated 
                    &#x201C;<span class="date-1827-02-16">16 Feb<sup>y</sup> 1827</span>&#x201D; (a Friday), which would have been about the date when 
                    <span class="persName-JJR">John James Ruskin</span> departed home to collect sherry orders in <span class="placeName-BIGGLESWADE">Biggleswade</span>, 
                    <span class="placeName-HUNTINGDON">Huntingdon</span>, <span class="placeName-KINGSLYNN">King&#x02BC;s Lynn</span>, <span class="placeName-NORWICH">Norwich</span>, 
                    and <span class="placeName-COLCHESTER">Colchester</span>, returning home on <span class="date-1827-03-01">1 March 1827</span> 
                    (<a href="/notes/bibliography#RFL" target="_self">Burd, ed., <i>Ruskin Family Letters</i></a>, 153 n. 1). It is tempting to think 
                    of <span class="persName-JR">Ruskin</span> composing a poem about a ship in mid&#x2010;February because he anticipated his father traveling to the seaport town of 
                    <span class="placeName-KINGSLYNN">King&#x02BC;s Lynn</span>. The presentation copy may have been made then or at a somewhat later time, 
                    explaining the uncertain dating in <span class="persName-JR">John</span>&#x02BC;s hand of <span class="date-1827-02-1827-03">February or March</span>, 
                    the period spanning <span class="persName-JJR">John James Ruskin</span>&#x02BC;s travels.
                     </div>
                     
                     <div class="p">It is tempting, too, to think of <span class="persName-JJR">John James Ruskin</span>&#x02BC;s journey in <span class="placeName-EASTANGLIA">East Anglia</span> as prompting composition of 
                    <a href="/apparatuses/on_papas_leaving_home_apparatus" target="_self"><span class="title-poem-ONPAPASLEAVINGHOME">&#x201C;On Papa&#x02BC;s Leaving Home&#x201D;</span></a> 
                    in <a href="/apparatuses/msi_poetry_anthology_apparatus" target="_self"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>, 
                    since <span class="date-1827-02">February 1827</span> falls within this anthology&#x02BC;s possible dating range. These poems responding to <span class="persName-JJR">John James Ruskin</span>&#x02BC;s 
                    absence could therefore be viewed as <span class="persName-JR">John</span>&#x02BC;s side of a father/son dynamic that produced the immediate context leading to the 
                    <span class="persName-JJR">father</span>&#x02BC;s accolade <span class="persName-JR">the boy</span> as &#x201C;the most intellectual creature of his age that ever appeared 
                    in any age&#x201D; (<a href="/apparatuses/jjr_to_mr_1827-02-21_al_apparatus" target="_self"><span class="title-letter-jjr_to_mr_1827-02-21">letter of <span class="date-1827-02-21">21 February 1827</span></span></a> &#x005B;<a href="/notes/bibliography#RFL" target="_self">Burd, ed., <i>Ruskin Family Letters</i></a>, 152&#x2013;53&#x005D;).
                     </div>
                     
                     <div class="p">The <a href="/apparatuses/msiii_apparatus" target="_self"><span class="title-manuscript-MSIII">MS III</span></a> witness, 
                    entitled &#x201C;<span class="caps">THE SHIP</span>&#x201D; and with &#x201C;<span class="caps">L</span><span class="smallcaps">ook</span> at that ship&#x201D; forming its first line, 
                    must belong to activities almost a year later, since it immediately follows poems dated <span class="date-1828-01-01">1 January 1828</span>.
                     </div>
                     
                     <div class="p">Another poem, <a href="/apparatuses/the_ship_apparatus" target="_self"><span class="title-poem-JRTHESHIP182829">&#x201C;The Ship&#x201D; &#x005B;1828&#x2013;29&#x005D;</span></a>, which is likewise contained in 
                    <a href="/apparatuses/msiii_apparatus" target="_self"><span class="title-manuscript-MSIII">MS III</span></a> (as part of the 
                    <a href="/apparatuses/msiii_poetry_anthology_3_apparatus" target="_self"><span class="title-anthology-MSIIIPOETRYANTHOLOGY3">MS III Third Poetry Anthology</span></a>), 
                    might also be included here as a later witness of the <span class="date-1827">1827</span> poem. Beyond the first three lines, however, the <span class="date-1828-1829">1828&#x2013;29</span> poem differs so
                    substantially that it should be treated as a distinct work. In the later poem, <span class="persName-JR">Ruskin</span>&#x02BC;s intentions have shifted toward expressing a patriotism that is
                    wholly absent from the earlier work, and that considerably expands the length and rhetorical range of the poem. At the same time, some rhetorical features of
                    the earlier poem remain present in the later one, if not identically expressed, such as <span class="persName-JR">Ruskin</span>&#x02BC;s interest in relevant sizes and distances. The <span class="date-1827">1827</span> and
                    <span class="date-1828-1829">1828&#x2013;29</span> pieces raise an interesting case for probing the distinction between <i>work</i> and <i>witness</i>, and it
                    is quite possible to see <span class="date-1828-1829">1828&#x2013;29</span> as part of the unfolding intentions of one witness to another, in a very open&#x2010;ended conception of a single work called &#x201C;The Ship&#x201D;.
                     </div>
                     
