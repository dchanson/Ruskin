                  <div id="TITLE" class="section">
                     <span class="head-section">Title</span>
                     
                     <div class="p">MS IX</div>
                     
                     <div class="p"><a href="/notes/collingwood_w_g_note" target="_self"><span class="persName-WGC">W. G. Collingwood</span></a> assigned
                        roman numeral IX to this notebook in his <span class="title-bibliography_text-COLLINGWOODPRELIMINARYNOTE">&#x201C;Preliminary Note on the Original MSS. of the Poems&#x201D;</span>
                        (<a href="/notes/bibliography#POEMS4D1891" target="_self"><i>Poems</i> &#x005B;4<sup>o</sup>, <span class="date-1891">1891</span>&#x005D;</a>,
                        1:265&#x2013;66; <a href="/notes/bibliography#POEMS8D1891" target="_self"><i>Poems</i> &#x005B;8<sup>o</sup>, <span class="date-1891">1891</span>&#x005D;</a>, 1:266&#x2013;67).
                     </div>
                     
                  </div>
                  
