                  <div id="PROVENANCE" class="section">
                     <span class="head-section">Provenance</span>
                     
                     <div class="p">See <a href="/notes/provenance_sothebys_1930_note" target="_self">Sotheby&#x02BC;s Sale of <span class="persName-JR">Ruskin</span> Manuscripts and Library, <span class="date-1930">1930</span></a>. 
                        See also <a href="/notes/provenance_american_research_libraries_note" target="_self">Ruskin Manuscript Collecting by American Research Libraries</a>.
                     </div>
                     
                     <div class="p">On the first leaf following the front flyleaf, <span class="persName-JR">Ruskin</span>&#x02BC;s cousin, <span class="persName-JAS">Joan Severn</span>, 
                        inscribed &#x201C;<span class="persName-JAS">Joan R. Severn</span> / from <span class="persName-pen_name-JR">Di Pa</span>&#x201D;,  
                        this manuscript being one of the those associated with touring that <span class="persName-JR">Ruskin</span> set aside for her (see Introduction. 
                        Opposite this inscription, on the verso of the flyleaf, is inscribed in pencil &#x201C;Mr Ruskin&#x201D; possibly in the hand of <span class="persName-JJR">John James Ruskin</span>, 
                        followed by a second line in the same hand, which is difficult to decipher, but which contains the numeral &#x201C;16&#x201D;.
                     </div>
                     
