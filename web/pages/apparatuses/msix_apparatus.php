                  <div id="DESCRIPTION" class="section">
                     <span class="head-section">Description</span>
                     
                     <div class="p">Ruled notebook; brown marbled boards and endpapers, three&#x2010;quarter&#x2010;bound in bluish&#x2010;green leather; 20.5 &#x00D7; 16.5 cm.</div>
                     
                     <div class="p">Someone&#x2014;probably <span class="persName-AW">Wedderburn</span>&#x2014;has numbered leaves on their rectos, starting, oddly, with the second leaf following the two front flyleaves; 
                        i.e., the inside first flyleaf has the inscription by (possibl) <span class="persName-JJR">John James</span> mentioned above, the second flyleaf has the inscription by <span class="persName-JAS">Joan Severn</span> mentioned above, 
                        and then there is another unnumbered leaf before the numbering begins on the following leaf. Despite this irregularity, I employ this numbering for the  Content  below. 
                        In the front of the notebook, eleven leaves (i.e., the first unnumbered leaf following the inscribed flyleaves, plus numbered leaves 1&#x2013;10) have had their right edges cut away&#x2014;to 
                        about one cm. deep&#x2014;but leaving a small tab on the edge of each leaf. On these marginal tabs, someone has printed an alphabet, prepatory for an alphabetical directory of some kind. 
                        These leaves were never used, however, and it is impossible to say whether the alphabetizing was done by <span class="persName-JR">Ruskin</span> or by someone who had prepared the notebook before him. 
                        Possibly, <span class="persName-JR">Ruskin</span> meant to provide an alphbetized table of contents at the start of his poem. (He did compile an index at the back of <span class="title-manuscript-MSVI">MS VI</span>, 
                        although that index was probably for his personal use, since <span class="title-manuscript-MSVI">MS VI</span> was a rough copy notebook.) As it turned out, <span class="persName-JR">Ruskin</span> failed to prepare even a title page for <span class="title-manuscript-MSIX">MS IX</span>, 
                        and the illustrated poem and prose sequence is untitled.
                     </div>
                     
                     <div class="p">It is also possible that, since <span class="persName-MR">Margaret Ruskin</span>&#x02BC;s writing is the first to appear in the manuscript ( Content,  d), she made the alphabetical tabs for some use of her own. 
                        The paper is watermarked  HAGAR &#x0026; CO / <span class="date-1822">1822</span>,  so the notebook might have lain unused in the household for a long time. What seems least likely is that 
                        someone would have subjected the manuscript to this treatment after <span class="persName-JR">Ruskin</span> used the notebook for his poetry&#x2014;or, for <span class="persName-JR">Ruskin</span>&#x02BC;s part, 
                        that he would have started his project several leaves into the notebook, leaving all those blank leaves to be exploited later.
                     </div>
                     
                     <div class="p">Tipped in at the front, attached to the marbled fly leaf, <span class="persName-AW">Wedderburn</span> added a sheet describing <span class="title-manuscript-MSIX">MS IX</span>. Like his sheets tipped into the front of many <span class="title-manuscript-JRREDBOOK">Red Books</span> 
                        and other major manuscripts, he pasted onto it a clipping from PJR describing the <span class="title-manuscript-MSIX">MS IX</span>, and commented on any additions or rebinding since <span class="persName-WGC">Collingwood</span>&#x02BC;s <span class="date-1891">1891</span> edition. 
                        In this case, where <span class="persName-WGC">Collingwood</span> summarized that  pp. 1&#x2013;21 are prepared for alphabetical index,  while  pp. 22&#x2013;23 contain quotations in <span class="persName-MR">Mrs. J. J. Ruskin</span>&#x2019;s handwriting,  
                        and  pp. 25&#x2013;111, about a third of the book, were filled with prose and verse . . . with inserted drawings &#x2014;i.e., the so&#x2010;called <span class="title-composite-JRACCOUNTTOUR">Account of a Tour on the Continent</span>&#x2014;<span class="persName-AW">Wedderburn</span> 
                        added the note,  most of the blank pages have since been removed &#x0026; the book replaced in its old cover.  This ranks among the more damaging decisions about the treatment 
                        of the early manuscripts. With blank leaves removed, we are prevented from seeing clearly where <span class="persName-JR">Ruskin</span> might have left gaps in his fair&#x2010;copying, intending to insert a passage or 
                        a drawing later&#x2014;a practice that remains obvious in the extant arrangement. One wonders, too, whether the leaves were entirely blank; even a roughly penciled notation 
                        might have provided clues about <span class="persName-JR">Ruskin</span>&#x02BC;s plan.
                     </div>
                     
                     <div class="p">Reconstruction of the original manuscript is rendered uncertain, since one cannot be absolutely certain how many leaves were removed. 
                        <span class="persName-WGC">Collingwood</span>&#x2019;s page counts in PJR accurately describe <span class="title-manuscript-MSIX">MS IX</span> in its present condition. This means that <span class="persName-WGC">Collingwood</span> skipped over blank leaves when numbering the manuscript&#x2014;a practice 
                        consistent with his treatment of blank leaves in MS IVD&#x2014;and these unnumbered leaves would have been the  pages  removed by <span class="persName-AW">Wedderburn</span>. Stubs do remain visible, 
                        but one cannot be confident that stubs would show for all the removed leaves, especially if the manuscript was removed and then rebound in its cover. 
                        In  Content  below, I have noted where a stub remains visible, which one may interpret as a leaf (i.e., two pages) that <span class="persName-JR">Ruskin</span> presumably intended to fill with writing or drawing. 
                        One must bear in mind, however, that not all stubs may be visible. The remaining stubs of the removed leaves should not be confused with stubs of a different paper&#x2014;paper 
                        that is not ruled, as the original paper is, and protruding slightly beyond the original marbled edges. These stubs appear to have been inserted as part of the rebinding process, 
                        to which <span class="persName-AW">Wedderburn</span> alludes in his tipped&#x2010;in sheet. As irrelevant to the notebook <span class="persName-JR">Ruskin</span> used, these rebinding stubs are not listed in  Content  below. 
                        Only the stubs are mentioned of paper that clearly belonged to the original notebook.
                     </div>
                     
                     
                     <div id="COLLATION" class="subsection">
                        <span class="head-subsection">Collation</span>
                        
                        <div class="p">In the <a href="/notes/library_edition_note" target="_self">Library Edition</a>, 
                            the editors document that, as the contents of the &#x201C;<span class="title-composite-JRACCOUNTTOUR">Account</span>&#x201D; were collated in their time, a gallery of drawings followed the last page of extant fair&#x2010;copied text. 
                            Hanging a note to that effect at the end of the poem, <span class="title-poem-JRHEIDELBERG">&#x201C;Now from the smiling afternoon&#x201D; &#x005B;&#x201C;Heidelberg&#x201D;&#x005D;</span>, 
                            the editors comment: &#x201C;In the MS. fair copy (ix.) two pages and a half are here left blank, and then follow 
                            the first four and a half lines of the prose passage . . . , the rest of which is supplied &#x005B;by the editors&#x005D; in the text from the draft in the earlier MS. (viii.).&#x201D; 
                            That is, following the end of the poem on 46v, a leaf 47r&#x2010;v is left blank, with the essay <span class="title-essay-JRHEIDELBERGPROSE">&#x201C;Most beautiful are the paths&#x201D; &#x005B;&#x201C;Heidelberg&#x201D;&#x005D;</span> 
                            starting half&#x2010;way down on 48r, but breaking off midway in the first sentence of the essay, 
                            after the word &#x201C;sometimes&#x201D;. Here <span class="persName-JR">Ruskin</span> stopped fair&#x2010;copying the &#x201C;Account&#x201D;, 
                            the editors supplying the remaining text of the work from rough draft in <span class="title-manuscript-MSVIII">MS VIII</span> and other sources. 
                            In <span class="title-manuscript-MSIX">MS IX</span>, the editors go on, &#x201C;&#x005B;t&#x005D;he fair copy ends at this point, so far as writing is concerned, but seven page&#x2010;sketches are inserted: 
                            (1) A mountain gorge; (2) mountain heights, a castle on one; (3) a river between steep banks, snow mountains in the distance; 
                            (4) a mountain scene, châlet in foreground; (5) a, a mountain scene, b, a lake with a house on piers islanded on it; 
                            (6) a lake&#x2010;side, with terraced gardens, hills behind; (7) aiguilles. These sketches belong to the later portion of the &#x201C;<span class="title-composite-JRACCOUNTTOUR">Tour</span>&#x201D;, 
                            of which the author did not make a fair copy.&#x201D; As discussed in the apparatus to the &#x201C;Account&#x201D; (<a href="/apparatuses/account_of_a_tour_on_the_continent_apparatus#MAJORMS" target="_self">Manuscripts</a>), 
                            it is not possible to determine whether <span class="persName-JR">Ruskin</span> himself assembled this gallery or another person did so when the manuscript was rebound.
                        
                        </div>
                        
