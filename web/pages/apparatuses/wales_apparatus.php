                  <div id="DATE" class="section">
                     <span class="head-section">Date</span>
                     
                     <div class="p"><span class="date-1827-05">May 1827</span> or possibly somewhat earlier; see 
                        <a href="/apparatuses/spring_blank_verse_apparatus#DATE" target="_self"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span>: Date</a>.
                     </div>
                     
                     <div class="p"><span class="persName-JR">Ruskin</span> copied <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span> and 
                        <a href="/apparatuses/spring_blank_verse_apparatus" target="_self"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a> 
                        into a <a href="/apparatuses/jr_to_jjr_1827-05_al_apparatus" target="_self"><span class="title-letter-">letter</span> to <span class="persName-JJR">his father</span> of <span class="date-1827-05">May 1827</span></a>, presumably to show them off as recent compositions. 
                        This date thus provides a lower limit for the poems&#x02BC; fair copies contained in the
                        <a href="/notes/poetry_discriptive_note" target="_self"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a> section of 
                        <a href="/apparatuses/msiii_apparatus" target="_self"><span class="title-manuscript-MSIII">MS III</span></a>, in which these two poems are placed between
                        <a href="/apparatuses/nature_apparatus" target="_self"><span class="title-poem-NATURE">&#x201C;Nature&#x201D;</span></a> and
                        <a href="/apparatuses/the_hill_of_kinnoul_apparatus" target="_self"><span class="title-poem-THEHILLOFKINNOUL">&#x201C;The Hill of Kinnoul&#x201D;</span></a>
                        (and in reverse order from their appearance in the <a href="/apparatuses/jr_to_jjr_1827-05_al_apparatus" target="_self"><span class="date-1827">1827</span> <span class="title-letter-">letter</span></a>&#x2014;that is, ordered in the anthology as first 
                        <a href="/apparatuses/spring_blank_verse_apparatus" target="_self"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>
                        followed by <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span>).
                     </div>
                     
                     <div class="p">In the <a href="/notes/library_edition_note" target="_self">Library Edition</a>, the editors attribute the entire anthology, 
                        <a href="/notes/poetry_discriptive_note" target="_self"><span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span></a>, to an earlier date, 
                        <span class="date-1826-09">September 1826</span>, assigned to just one of its poems, 
                        <a href="/apparatuses/glen_of_glenfarg_apparatus" target="_self"><span class="title-poem-GLENOFGLENFAR">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</span></a>; however, the 
                        earliest possible date of these poems (in their <a href="/apparatuses/msiii_apparatus" target="_self"><span class="title-manuscript-MSIII">MS III</span></a> witnesses, at any rate) is established by
                        <span class="persName-JR">Ruskin</span>&#x02BC;s <a href="/apparatuses/jr_to_jjr_1827-05_al_apparatus" target="_self"><span class="date-1827-05">May 1827</span> <span class="title-letter-">letter</span></a> to his <span class="persName-JJR">father</span>.
                        Moreover, <span class="persName-JR">Ruskin</span> printed the entire <span class="title-anthology-POETRYDISCRIPTIVE">&#x201C;Poetry Discriptive&#x201D;</span> anthology in ink, 
                        which he had first used only in <span class="date-1827-04-20-1827-04-30">late April 1827</span>&#x2014;evidence confirmed both by his <span class="persName-">mother</span>&#x02BC;s 
                        testimony in a <a href="/apparatuses/mr_to_jjr_1827-04-28_al_apparatus" target="_self"><span class="title-letter-">letter</span> of <span class="date-1827-04-28">April 28</span></a> and by the fact that the pair of poems 
                        in <span class="persName-JR">Ruskin</span>&#x02BC;s <a href="/apparatuses/jr_to_jjr_1827-05_al_apparatus" target="_self"><span class="date-1827-05">May 1827</span> <span class="title-letter-">letter</span></a>, <span class="title-poem-WALES">&#x201C;Wales&#x201D;</span> and 
                        <a href="/apparatuses/spring_blank_verse_apparatus" target="_self"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>, 
                        are printed in ink, while the <a href="/apparatuses/jr_to_jjr_1827-05_al_apparatus" target="_self"><span class="title-letter-">letter</span></a> itself is written in pencil (see
                        <a href="/notes/hand_note" target="_self">Ruskin&#x02BC;s Handwriting</a>). Thus, while the composition of 
                        <a href="/apparatuses/glen_of_glenfarg_apparatus" target="_self"><span class="title-poem-GLENOFGLENFAR">&#x201C;Glen of Glenfarg&#x201D; (&#x201C;Glen of Glenfarg thy beauteous rill&#x201D;)</span></a> 
                        by itself may date from <span class="date-1826-09">September 1826</span>, this earlier date does not apply to the remaining poems in the anthology&#x2014;some in fact relating to 
                        the summer tour of <span class="date-1827">1827</span>, on which the family departed sometime after May 
                        (see <a href="/notes/tours_of_1826-27_note" target="_self">Tours of 1826&#x2013;27</a>).
                    
                     </div>
                     
                     <div class="p">See also <a href="/notes/date_citation_note" target="_self">System of Date Citation</a>.</div>
                     
